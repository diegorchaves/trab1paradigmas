public class ExercicioTreino {
    Exercicio exercicio;
    Integer carga;
    Integer repeticoes_min;
    Integer repeticoes_max;
    Integer numero_series;
    Integer tempo_descanso;

    public Exercicio getExercicio() {
        return exercicio;
    }

    public void setExercicio(Exercicio exercicio) {
        this.exercicio = exercicio;
    }

    public Integer getCarga() {
        return carga;
    }

    public void setCarga(Integer carga) {
        this.carga = carga;
    }

    public Integer getRepeticoes_min() {
        return repeticoes_min;
    }

    public void setRepeticoes_min(Integer repeticoes_min) {
        this.repeticoes_min = repeticoes_min;
    }

    public Integer getRepeticoes_max() {
        return repeticoes_max;
    }

    public void setRepeticoes_max(Integer repeticoes_max) {
        this.repeticoes_max = repeticoes_max;
    }

    public Integer getNumero_series() {
        return numero_series;
    }

    public void setNumero_series(Integer numero_series) {
        this.numero_series = numero_series;
    }

    public Integer getTempo_descanso() {
        return tempo_descanso;
    }

    public void setTempo_descanso(Integer tempo_descanso) {
        this.tempo_descanso = tempo_descanso;
    }
}
